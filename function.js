//Create a function that can determine if a parameter is true or false
// if it is true return the string "true". Otherwise, return the string "false"

function trueOrFalse(value) {
  
    if (value === true) {
        return "true"
    } else if (value === false) {
        return "false"
    }

}
console.log(trueOrFalse(true))
